#Addition
def Addition(a,b):
    Answer = a + b
    return Answer

#Subtraction
def Subtraction(a,b):
    Answer = a - b
    return Answer

#Multiplication
def Multiplication(a,b):
    Answer = a * b
    return Answer

#Division
def Division(a,b):
    Answer = a/b
    return Answer

#Exponent
def SquareRooting(a,b):
    Answer = a**b
    return Answer

